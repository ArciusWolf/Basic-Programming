#include <iostream>
using namespace std;

int main() {
	int a, b;
	cout<<"Enter 1st number: ";
	cin>>a;
	cout<<"\nEnter 2nd number: ";
	cin>>b;
	cout<<"\na == b: "<<(a == b);
	cout<<"\na != b: "<<(a != b);
	cout<<"\na < b: "<<(a < b);
	cout<<"\na > b: "<<(a > b);
	cout<<"\na <= b: "<<(a <= b);
	cout<<"\na >= b: "<<(a >= b);
	
	return 0;
}
