#include<iostream>
using namespace std;

int main () {
	int a, b, Tong, Hieu, ThuongNguyen, ThuongDu, Tich;
/*-----Khai báo Variable-----*/
	a = 10;
	b = 5;			
	Tong = a + b;		
	Hieu = a - b;		
	ThuongNguyen = a / b;	
	ThuongDu = a % b;	
	Tich = a * b;		
	
	cout<<"Tong = "<<Tong;
	cout<<"\nHieu = "<<Hieu;
	cout<<"\nThuongNguyen = "<<ThuongNguyen;
	cout<<"\nThuongDu = "<<ThuongDu;
	cout<<"\nTich = "<<Tich;
	
	return 0;
	
}
